function draw() {
    background(0);

    for (let i = 0; i < cols; i++) {
        for (let j = 0; j < rows; j++) {
            let x = i * resolution;
            let y = j * resolution;
            if (gridArr[i][j] == 1) {
                fill(255);
                stroke(0);
                rect(x, y, resolution - 1, resolution - 1);
            }
        }
    }
    if (started){


        let next = make2DArray(cols, rows);

        for (let i = 0; i < cols; i++) {
            for (let j = 0; j < rows; j++) {
                let state = gridArr[i][j];
                // get the neighbors
                let sum = 0;
                let neighbors = getNeighbors(gridArr, i, j);

                if (state == 0 && neighbors == 3) {
                    next[i][j] = 1;
                } else if (state == 1 && (neighbors < 2 || neighbors > 3)) {
                    next[i][j] = 0;
                } else {
                    next[i][j] = state;
                }
            }
        }
        gridArr = next;
    }
}

function getNeighbors(gridArr, x, y) {
    let totalNeighbors = 0;
    for (let i = -1; i < 2; i++) {
        for (let j = -1; j < 2; j++) {
            let row = (y + j + rows) % rows;
            let col = (x + i + cols) % cols;
            totalNeighbors += gridArr[col][row];
        }
    }
    totalNeighbors -= gridArr[x][y];
    return totalNeighbors;
}
